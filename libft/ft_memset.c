#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*m;

	i = 0;
	m = (char *)s;
	while (i < n)
	{
		m[i] = c;
		i++;
	}
	return (s);
}
